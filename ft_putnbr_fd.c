#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		if (n == -2147483647)
		{
			ft_putchar_fd('2', 1);
			n = -147483647;
		}
		n *= -1;
	}
	
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	if (n > -1 && n < 10)
		ft_putchar_fd(n + 48,fd);
}